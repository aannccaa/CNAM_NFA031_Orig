/*
Concatener 2 chaines en utilsant la fonction concat
 */
public class NFA031_Mod6_2b1_Chaines_concat {

	public static void main(String[] args) {
		String s1, s2;
		String stringConcatenne;
		
		s1 = "program";
		s2 = "mation";
		stringConcatenne = s1.concat(s2);
		
		System.out.println("La concatenation de s1 et s2 est :" + stringConcatenne);
	}

}
