/*
Concatener 2 chaines en utilisant le + 
 */
public class NFA031_Mod6_2b2_Chaines_concatPlus {

	public static void main(String[] args) {
		String s1, s2;
		String stringConcatenne;
		
		s1 = "program";
		s2 = "mation";
		stringConcatenne = s1 + s2;
		
		System.out.println("La concatenation de s1 et s2 est :" + stringConcatenne);
	}

}
